#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*ps;
	size_t			i;

	ps = s;
	i = 0;
	while (i < n)
	{
		ps[i] = (unsigned char) '\0';
		i++;
	}
}
